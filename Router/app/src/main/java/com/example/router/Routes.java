package com.example.router;

public class Routes {
    String routesType;
    String routesStart;
    String routesEnd;
    String routesTime;

    public Routes(String routesType, String routesStart, String routesEnd, String routesTime) {
        this.routesType = routesType;
        this.routesStart = routesStart;
        this.routesEnd = routesEnd;
        this.routesTime = routesTime;
    }
}
